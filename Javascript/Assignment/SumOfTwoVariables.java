public class SumOfTwoVariables {
    public static void main(String[] args) {
        // Declare and initialize the variables
        int num1 = 5; // Example value for x
        int num2 = 10; // Example value for y

        // Calculate the sum of x and y
        int sum = num1 + num2;

        // Calculate twice the value of the sum
        int twiceSum = 2 * sum;

        // Output the result
        System.out.println("Twice the value of the sum of " + x + " and " + y + " is " + twiceSum);
    }
}
